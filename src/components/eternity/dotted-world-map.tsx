interface DeathPulse {
  id: number;
  x: number;
  y: number;
}

export function DottedWorldMap() {
  const [pulses, setPulses] = useState<DeathPulse[]>([]);
  const nextId = useRef(0);
  const intervalRef = useRef<ReturnType<typeof setInterval>>(null);

  const addPulse = useCallback(() => {
    const dot = POPULATED_DOTS[Math.floor(Math.random() * POPULATED_DOTS.length)];
    // Add slight randomness to position so pulses don't stack exactly
    const x = dot[0] + (Math.random() - 0.5) * 8;
    const y = dot[1] + (Math.random() - 0.5) * 8;
    const id = nextId.current++;

    setPulses((prev) => [...prev.slice(-12), { id, x, y }]);
  }, []);

  useEffect(() => {
    // Add a pulse every ~550ms (slightly faster than 1 death/sec for visual density)
    intervalRef.current = setInterval(addPulse, 550);
    // Start with one immediately
    addPulse();

    return () => {
      if (intervalRef.current) clearInterval(intervalRef.current);
    };
  }, [addPulse]);

  return (
    <div className="relative w-full max-w-3xl mx-auto">
      <svg
        viewBox="0 0 600 340"
        className="w-full h-auto"
        aria-hidden="true"
      >
        {/* Static continent dots */}
        {CONTINENT_DOTS.map(([x, y], i) => (
          <circle
            key={i}
            cx={x * 0.9 - 60}
            cy={y * 0.9 - 30}
            r={1.2}
            fill="white"
            opacity={0.12}
          />
        ))}

        {/* Death pulse animations */}
        <AnimatePresence>
          {pulses.map((pulse) => (
            <g key={pulse.id}>
              {/* Expanding ring */}
              <motion.circle
                cx={pulse.x * 0.9 - 60}
                cy={pulse.y * 0.9 - 30}
                r={1.5}
                fill="none"
                stroke="#DC2626"
                strokeWidth={0.8}
                initial={{ r: 1.5, opacity: 0.8 }}
                animate={{ r: 12, opacity: 0 }}
                exit={{ opacity: 0 }}
                transition={{ duration: 2, ease: "easeOut" }}
              />
              {/* Center dot */}
              <motion.circle
                cx={pulse.x * 0.9 - 60}
                cy={pulse.y * 0.9 - 30}
                r={2}
                fill="#DC2626"
                initial={{ opacity: 0.9, scale: 1 }}
                animate={{ opacity: 0, scale: 0.3 }}
                exit={{ opacity: 0 }}
                transition={{ duration: 2, ease: "easeOut" }}
              />
            </g>
          ))}
        </AnimatePresence>
      </svg>
    </div>
  );
}
